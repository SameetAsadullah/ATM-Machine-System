    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int amount = 0;
        try {
            amount = Integer.parseInt(jTextField1.getText());
            if (amount % 5 != 0) {
                jLabel2.setText("Kindly Enter Correct Amount i.e. divisible by 5");
            } else {
                ATM atm = ATM.getInstance();
                int flag = atm.canWithdrawMoney(accNo, amount);

                if (flag == 0) {
                    jLabel4.setText("Not enough money in account");
                }
                else if (flag == 1) {
                    this.dispose();
                    new WithdrawInfo(accNo, amount).setVisible(true);
                }
                else {
                    jLabel4.setText("Not enough money in ATM");
                }
            }
        } catch (Exception e) {
            jLabel2.setText("Kindly Enter Correct Amount i.e. divisible by 5");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

